    /**
     * Parse string input to enum.
     * @param input zip code to parse
     * @return The parsed US state, or null on failure.
     */
    public static USZipCodes parse(String zipCode) {
        if ((zipCode == null) || (zipCode.length() < 5)) {
            return null;
        }
        zipCode = zipCode.trim();
        String prefix = StringUtils.left(zipCode, 3);
        for (USZipCodes zip : values()) {
            if (zip.zipPrefix.equalsIgnoreCase(prefix)) {
                return zip;
            }
        }
        return null;
    }
